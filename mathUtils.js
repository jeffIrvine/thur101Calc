export function addTwoNumbers(someNumberToAdd, someOtherNumberToAdd) {
    return someNumberToAdd + someOtherNumberToAdd;
}
export function subTwoNumbers(someNumberToSub, someOtherNumberToSub) {
    return someNumberToSub - someOtherNumberToSub;
}
export function multTwoNumbers(someNumberToMult, someOtherNumberToMult) {
    return someNumberToMult * someOtherNumberToMult;
}
export function divTwoNumbers(someNumberToDiv, someOtherNumberToDiv) {
    return someNumberToDiv / someOtherNumberToDiv;
}